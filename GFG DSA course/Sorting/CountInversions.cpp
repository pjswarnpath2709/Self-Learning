// naive aproach O(N*N)
int CountInversionNaive(int *arr, int n)
{
    int count = 0;
    for (int i = 0; i < n; i++)
    {
        for (int j = i + 1; j < n; j++)
        {
            if (arr[i] > arr[j])
            {
                count++;
            }
        }
    }
    return count;
}
// efficient approach
int MergeCountInversion(int arr[], int l, int m, int r)
{
    int n1 = m - l + 1, n2 = r - m;
    int left[n1], right[n2];
    for (int i = 0; i < n1; i++)
        left[i] = arr[i + l];
    for (int j = 0; j < n2; j++)
        right[j] = arr[m + 1 + j];
    int res = 0, i = 0, j = 0, k = l;
    while (i < n1 && j < n2)
    {
        if (left[i] <= right[j])
        {
            arr[k++] = left[i++];
        }
        else
        {
            arr[k++] = right[j++];
            res = res + (n1 - i);
        }
    }
    while (i < n1)
        arr[k++] = left[i++];
    while (j < n2)
        arr[k++] = right[j++];
    return res;
}
int MergeInversionSort(int arr[], int low, int high)
{
    int ans = 0;
    if (low < high)
    {
        int mid = low + (high - low) / 2;
        ans = MergeInversionSort(arr, low, mid);
        ans += MergeInversionSort(arr, mid + 1, high);
        ans += MergeCountInversion(arr, low, mid, high);
    }
    return ans;
}
