bool is_sorted(vll &vec)
{
    for (int i = 0; i < vec.size(); i++)
    {
        if (vec[i] > vec[i + 1])
        {
            return false;
        }
    }
    return true;
}
double medanOfTwoSortedArrays(vi &vec1, vi &vec2)
{
    if (vec2.size() < vec1.size())
        return medanOfTwoSortedArrays(vec2, vec1);
    int n1 = vec1.size();
    int n2 = vec2.size();
    int low = 0, high = n1;
    while (low <= high)
    {
        int cut1 = (low) + (high - low) / 2;
        int cut2 = (n1 + n2 + 1) / 2 - cut1;

        int left1 = cut1 == 0 ? INT_MIN : vec1[cut1 - 1];
        int left2 = cut2 == 0 ? INT_MIN : vec2[cut2 - 1];

        int right1 = cut1 == n1 ? INT_MAX : vec1[cut1];
        int right2 = cut2 == n2 ? INT_MAX : vec2[cut2];
        if (left1 <= right2 && left2 <= right1)
        {
            if ((vec1.size() + vec2.size()) % 2 == 0)
            {
                return (double)max(left1, left2);
            }
            return (double)((max(left1, left2)) + min(right1, right2)) / 2;
        }
        else if (left1 > right2)
        {
            high = cut1 - 1;
        }
        else
        {
            low = cut1 + 1;
        }
    }
    return 0.0;
}
